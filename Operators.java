class Operators{ 
public static void main(String []args){ 
int p=3, q =6 ; 
System.out.println("p = "+p+", q = "+q); 
System.out.println("Addition : "+(p+q)); 
System.out.println("Substraction : "+(p-q)); 
System.out.println("Multiplicaion : "+(p*q)); 
System.out.println("Division : "+(p/q)); 
System.out.println("Remainder : "+(p%q)); 

q=p; 
System.out.println("q After assigning p : "+q); 

p++; 
System.out.println("a Incremented using ++ : "+p); 

p--; 
System.out.println("a is decremented using -- : "+p); 
} 
} 
